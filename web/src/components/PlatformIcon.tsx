export function PlatformIcon({
  platform,
  size = 16,
  className,
  ...rest
}: { platform?: string | null } & Omit<LucideProps, 'ref'>) {
  if (platform === 'web') {
    return <Globe size={size} className={className} style={{ color: '#2196F3' }} {...rest} />;
  }

  const brand = platform ? brandPaths[platform] : null;
  if (brand) {
    return (
      <svg
        xmlns="http://www.w3.org/2000/svg"
        width={size}
        height={size}
        viewBox="0 0 24 24"
        fill={brand.color}
        className={className}
        {...rest}
      >
        <path d={brand.d} />
      </svg>
    );
  }

  return <MonitorSmartphone size={size} className={className} {...rest} />;
}
